public class Operations {
    public static void main(String[] args) {
        int num1 = 35;
        int num2 = 7;
        // Adição
        System.out.println(num1 + num2);
        // Subtração
        System.out.println(num1 - num2);
        // Multiplicação
        System.out.println(num1 * num2);
        // Divisão (Inteiros)
        System.out.println(num1 / num2);
        // Resto
        System.out.println(num1 % num2);
        // Potenciação
        System.out.println(Math.pow(num1, num2));
        // Radiciação
        System.out.println(Math.sqrt(num1));
    }
}
